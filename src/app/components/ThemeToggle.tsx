export default function ThemeToggle() {
  const { theme, toggle } = useTheme();

  return (
    <button
      onClick={toggle}
      aria-label={theme === "dark" ? "Cambiar a modo claro" : "Cambiar a modo oscuro"}
      title={theme === "dark" ? "Modo claro" : "Modo oscuro"}
      className="inline-flex items-center justify-center rounded-lg border border-slate-500/30 bg-slate-700/50 p-2 text-slate-300 backdrop-blur-sm transition-all duration-200 hover:bg-slate-600/60 hover:text-white focus:outline-none focus:ring-2 focus:ring-sky-400/50 active:scale-95 dark:border-slate-500/40 dark:bg-slate-600/50 dark:hover:bg-slate-500/60"
    >
      {theme === "dark" ? <SunIcon /> : <MoonIcon />}
    </button>
  );
}
